public class Arguments_Parameters {
	// args = Parameter 매개변수 우리가 입력한 입력값을 String[] 문자열로 이루어진 array args에 저장한다.
	public static void main(String[] args) {
		String name = args[0];
		String score_1 = args[1];
		String score_2 = args[2];
		System.out.println(name);
		System.out.println(score_1);
		System.out.println(score_2);
	}

}
